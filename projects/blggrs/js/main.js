"use strict";

function _classCallCheck(instance, Constructor) { if (!(instance instanceof Constructor)) { throw new TypeError("Cannot call a class as a function"); } }

(function () {
  var MyClass;

  MyClass = function MyClass() {
    _classCallCheck(this, MyClass);
  };
}).call(void 0);